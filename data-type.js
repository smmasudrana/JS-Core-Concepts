// JavaScript Data Types

// 1. Primitive Data Types

let a = "Hello";       // String
let b = 42;            // Number
let c = true;          // Boolean
let d;                 // Undefined
let e = null;          // Null
let f = Symbol("id");  // Symbol (unique value)
let g = 23n;           // BigInt (very large integers)


// 2. Non-Primitive (Reference)
let obj = { name: "Rafid", age: 22 }; // Object
let arr = [1, 2, 3];                  // Array
function greet() { return "Hi"; }     // Function
let date = new Date();                // Built-in Object
